# Write a Python program to determine the largest and smallest integers, longs, and floats.

num_lst = [5, 10, 3, -7]
longs = [100000000, 200000000, 300000000]
float_lst = [3.14, 2.71, -1.5]

print(max(num_lst))
print(min(num_lst))
print(max(longs))
print(min(longs))
print(max(float_lst))
print(min(float_lst))
